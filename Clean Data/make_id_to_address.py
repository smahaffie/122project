			if name not in name_to_address:
				print(name[-11:])
				if name[-11:] == 'HIGH SCHOOL':
					if name[:-11] + "HS" in name_to_address:
						name = name[:-11] + 'HS'
						print('Case 1')
					elif name[:-12] in name_to_address:
						name = name[:-12]
			assert name in name_to_address
			w.writerow([sid, name_to_address[name]])
 

